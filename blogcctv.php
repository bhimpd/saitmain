                <p>AHD CCTV and IP CCTV are the two forms of CCTV that are often used. Analog High Definition closed-circuit television, or AHD CCTV, employs analog signals for video surveillance, as the name implies. Coaxial cables are used to transmit signals from the camera to DVRs (Digital Video Recorders). AHD CCTV, in the past, used to support video resolutions of 720p and 1080p only. However, thanks to technological advancements, AHD now supports 4K video resolutions. An IP camera, or Internet Protocol camera, is a form of digital video camera which uses an IP network to receive input and give output. The input is generally a series of control signals and the output is data in the form of images. Since IP CCTV cameras can be accessed via the internet, it is prone to hacking and is more vulnerable. On the contrary to that, the analog HD cctv doesn’t possess IP addresses, it is more secured. Also, the cost of the IP cameras is higher than the analog ones. However, it is easy to upgrade or downgrade those cameras once installed. The video quality of the IP cameras is also higher.</p>


